if(d==0):
	print("It is ZERO")
elif(d==1):
	print("It is ONE")
elif(d==2):
	print("It is TWO")
elif(d==3):
	print("It isTHREE")
elif(d==5):
	print("It is FIVE")
elif(d==4):
	print("It is FOUR")
elif(d==6):
	print("It is SIX")
elif(d==7):
	print("It is SEVEN")
elif(d==8):
	print("It is EIGHT")
elif(d==9):
	print("It is NINE")
else:
	print("Its not a DIGIT")

